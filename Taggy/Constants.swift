import Cocoa

extension NSColor {
    class var primary: NSColor {
        return NSColor(hex: "#1e8aff")!
    }
    
    class var secondary: NSColor {
        return NSColor(hex: "#ffffff")!
    }
    
    class var textPrimary: NSColor {
        return NSColor(hex: "#393e41")!
    }
    
    class var textSecondary: NSColor {
        return NSColor(hex: "#ffffff")!
    }
}
